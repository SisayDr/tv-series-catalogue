function hideTvShows() {
  for (var i = 0; i < tvShows.length; i++) {
    tvShows[i].style.display = "none";
  }
}
function paginate(pageNum) {
  hideTvShows();
  if (pageNum * 6 - tvShows.length > 6) {
    var Stop = tvShows.length;
  } else {
    var Stop = pageNum * 6;
  }
  for (var i = (pageNum - 1) * 6; i < Stop; i++) {
    tvShows[i].style.display = "block";
  }
}
paginate(1);
function filterByTitle(letter) {
  hideTvShows();
  //display back the matches
  var matches = 0;
  for (var i = 0; i < tvShows.length; i++) {
    var title = tvShows[i].getElementsByClassName("title")[0].innerHTML;
    if (title[0] == letter) {
      matches++;
      tvShows[i].style.display = "block";
    }
  }

  if (matches == 0) {
    document.getElementById("noResults").style.display = "block";
    document.getElementById("noResults").innerHTML =
      "No Result found for " + letter;
  } else {
    document.getElementById("noResults").innerHTML = "";
    document.getElementById("noResults").style.display = "none";
  }
}
